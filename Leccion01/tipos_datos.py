#Tipo de Dato Entero (INT):
'''
x = 10
print(x)
print(type(x))
'''

#Tipo de Dato Cadena o Texto (STRING):
'''
x = "Hola Mundo"
print(x)
print(type(x))
'''

'''
x:str = 'Hola Mundo'
print(x)
print(type(x))
'''

#Tipo de Dato Float (Decimal):
'''
x = 10.5
print(x)
print(type(x))
'''

#Tipo de Dato Boolean (Verdadero o Falso):
x = True
print(x)
print(type(x))